    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String roomNumber = jTextField9.getText().trim(); 

        if (roomNumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan Nomor Kamar terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            String query = "SELECT Price, Status FROM rooms WHERE Room_Number = ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, roomNumber);

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
            String status = rs.getString("Status");

            // Cek status kamar
            if ("booked".equalsIgnoreCase(status) || "not available".equalsIgnoreCase(status)) {
                jFormattedTextField4.setText("Kamar tidak tersedia");
            } else if ("available".equalsIgnoreCase(status)) {
                double price = rs.getDouble("Price");
                jFormattedTextField4.setText(String.format("Rp %,.2f", price));
            } else {
                jFormattedTextField4.setText("Status kamar tidak valid");
            }
        }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saat mengakses database.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

