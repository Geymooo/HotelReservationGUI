    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String roomType = jComboBox2.getSelectedItem().toString();

        if (roomType.isEmpty()) {
            jOptionPane1.showMessageDialog(this, "Pilih tipe kamar terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            String query = "SELECT Room_Number, Status, Price FROM rooms WHERE Type = ? AND Status = 'Available'";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, roomType);

            ResultSet rs = stmt.executeQuery();

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            boolean isAvailable = false;

            while (rs.next()) {
                isAvailable = true;
                String roomNumber = rs.getString("Room_Number");
                String status = rs.getString("Status");
                double price = rs.getDouble("Price");

                model.addRow(new Object[]{roomNumber, roomType, status, String.format("Rp %,.2f", price)});
            }

            if (!isAvailable) {
                jOptionPane1.showMessageDialog(this, "Tidak ada kamar yang tersedia untuk tipe ini.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            jOptionPane1.showMessageDialog(this, "Error saat memeriksa ketersediaan kamar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nik = jFormattedTextField1.getText();
        String roomNumber = jTextField9.getText().trim(); 
        String checkInDate = jTextField6.getText();
        String checkOutDate = jTextField7.getText();

        if (checkInDate.isEmpty() || checkOutDate.isEmpty() || nik.isEmpty() || roomNumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon lengkapi semua data!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            sdf.setLenient(false);
            sdf.parse(checkInDate);
            sdf.parse(checkOutDate);
        } catch (java.text.ParseException e) {
            JOptionPane.showMessageDialog(this, "Format tanggal salah! Gunakan format YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            String checkRoomQuery = "SELECT ID FROM rooms WHERE Room_Number = ? AND Status = 'Available'";
            PreparedStatement checkRoomStmt = conn.prepareStatement(checkRoomQuery);
            checkRoomStmt.setString(1, roomNumber);

            ResultSet rs = checkRoomStmt.executeQuery();
            if (rs.next()) {
                int roomId = rs.getInt("ID");

                String insertBookingQuery = "INSERT INTO bookings (user_id, room_id, status, check_in, check_out) " +
                                             "VALUES ((SELECT user_id FROM cust WHERE NIK = ?), ?, 'Pending', ?, ?)";
                PreparedStatement insertBookingStmt = conn.prepareStatement(insertBookingQuery);
                insertBookingStmt.setString(1, nik);
                insertBookingStmt.setInt(2, roomId);
                insertBookingStmt.setString(3, checkInDate);
                insertBookingStmt.setString(4, checkOutDate);

                int rowsInserted = insertBookingStmt.executeUpdate();
                if (rowsInserted > 0) {
                    String updateRoomQuery = "UPDATE rooms SET Status = 'Booked' WHERE ID = ?";
                    PreparedStatement updateRoomStmt = conn.prepareStatement(updateRoomQuery);
                    updateRoomStmt.setInt(1, roomId);
                    updateRoomStmt.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Reservasi berhasil dilakukan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal melakukan reservasi. Coba lagi.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nomor kamar tidak tersedia untuk tipe ini atau sudah dipesan.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengakses database.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

