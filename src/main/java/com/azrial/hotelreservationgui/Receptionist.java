    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Ambil nama yang dicari dari text field
        String searchedName = jTextField1.getText().trim();

        // Ambil status baru dari dropdown
        String newStatus = jComboBox1.getSelectedItem().toString();

        // Validasi input
        if (searchedName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan masukkan nama untuk mencari data terlebih dahulu.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Koneksi ke database dan update status
        try (Connection conn = DatabaseConnection.getConnection()) {
            // Query untuk update status booking berdasarkan nama
            String query = "UPDATE bookings " +
                           "INNER JOIN users ON bookings.user_id = users.ID " +
                           "SET bookings.Status = ? " +
                           "WHERE users.Name = ?";

            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, newStatus); // Set status baru
            stmt.setString(2, searchedName); // Filter berdasarkan nama yang dicari

            int rowsUpdated = stmt.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Status booking berhasil diperbarui!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data yang diperbarui. Pastikan nama sesuai.", "Warning", JOptionPane.WARNING_MESSAGE);
            }

            // Refresh tabel setelah update
            jButton1ActionPerformed(evt); // Panggil kembali fungsi search untuk memuat ulang tabel
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal memperbarui status booking.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

