    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String keyword = jTextField1.getText().trim();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);  

        try (Connection conn = DatabaseConnection.getConnection()) {
            String query = "SELECT users.Name, rooms.Room_Number, rooms.Type, bookings.Status, bookings.Check_In, bookings.Check_Out " +
                           "FROM bookings " +
                           "INNER JOIN users ON bookings.user_id = users.ID " +
                           "INNER JOIN rooms ON bookings.room_id = rooms.ID " +
                           "WHERE users.Name LIKE ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, "%" + keyword + "%"); 

            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                String name = rs.getString("Name");
                String roomNumber = rs.getString("Room_Number");
                String roomType = rs.getString("Type");
                String status = rs.getString("Status");
                String checkIn = rs.getString("Check_In");
                String checkOut = rs.getString("Check_Out");

                model.addRow(new Object[]{name, roomNumber, roomType, status, checkIn, checkOut});
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal mengambil data booking", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

