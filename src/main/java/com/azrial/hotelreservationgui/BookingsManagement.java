    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String userID = jTable1.getValueAt(selectedRow, 0).toString();
            String roomID = jTable1.getValueAt(selectedRow, 1).toString();

            int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try (Connection conn = DatabaseConnection.getConnection()) {
                    // Menghapus data booking
                    String deleteBookingQuery = "DELETE FROM bookings WHERE User_ID = ? AND Room_ID = ?";
                    PreparedStatement deleteStmt = conn.prepareStatement(deleteBookingQuery);
                    deleteStmt.setInt(1, Integer.parseInt(userID));
                    deleteStmt.setInt(2, Integer.parseInt(roomID));

                    int rowsDeleted = deleteStmt.executeUpdate();

                    if (rowsDeleted > 0) {
                        String updateRoomStatusQuery = "UPDATE rooms SET Status = 'Available' WHERE ID = ?";
                        PreparedStatement updateStmt = conn.prepareStatement(updateRoomStatusQuery);
                        updateStmt.setInt(1, Integer.parseInt(roomID));
                        updateStmt.executeUpdate();

                        JOptionPane.showMessageDialog(this, "Data berhasil dihapus dan status kamar diperbarui!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                        loadBookingsData();
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Kesalahan database", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
    }//GEN-LAST:event_jButton6ActionPerformed

