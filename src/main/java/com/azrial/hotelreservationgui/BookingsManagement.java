    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih data yang akan diperbarui!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String userID = jTable1.getValueAt(selectedRow, 0).toString();
            String roomID = jTable1.getValueAt(selectedRow, 1).toString();
            String status = jComboBox1.getSelectedItem().toString();
            String checkInDate = jFormattedTextField1.getText();
            String checkOutDate = jFormattedTextField2.getText();

            try (Connection conn = DatabaseConnection.getConnection()) {
                String query = "UPDATE bookings SET Status = ?, Check_In = ?, Check_Out = ? WHERE User_ID = ? AND Room_ID = ?";
                PreparedStatement stmt = conn.prepareStatement(query);
                stmt.setString(1, status);
                stmt.setDate(2, java.sql.Date.valueOf(checkInDate));
                stmt.setDate(3, java.sql.Date.valueOf(checkOutDate));
                stmt.setInt(4, Integer.parseInt(userID));
                stmt.setInt(5, Integer.parseInt(roomID));

                int rowsUpdated = stmt.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    loadBookingsData();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal memperbarui data!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Kesalahan database", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jButton2ActionPerformed

